    {
        double tutar=Double.parseDouble(this.jTextFieldTutar.getText());
        double pesinat=Double.parseDouble(this.jTextFieldPesinat.getText());
        double Taksit1=Double.parseDouble(this.jTextField1Taksit.getText());
        double Taksit2=Double.parseDouble(this.jTextField2Taksit.getText());
        double Taksit3=Double.parseDouble(this.jTextField3Taksit.getText());
        double Taksit4=Double.parseDouble(this.jTextField4Taksit.getText());
        double Taksit5=Double.parseDouble(this.jTextField5Taksit.getText());
        double Taksit6=Double.parseDouble(this.jTextField6Taksit.getText());
        double Taksit7=Double.parseDouble(this.jTextField7Taksit.getText());
        double Taksit8=Double.parseDouble(this.jTextField8Taksit.getText());
        if(tutar!=0)
        {

            if(tutar!=(pesinat+Taksit1+Taksit2+Taksit3+Taksit4+Taksit5+Taksit6+Taksit7+Taksit8))
            {
                JOptionPane.showMessageDialog(null, "Taksit Tutarlarının ve Peşinatın toplamı Tam Tutara eşit olmalıdır.", null , JOptionPane.DEFAULT_OPTION);            
                return false;
            }
            else
            {
                return true;
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Tam Tutar değeri sıfırdan büyük olmalıdır.", null , JOptionPane.DEFAULT_OPTION);                        
            return false;            
        }
    }
    
    
    
    
