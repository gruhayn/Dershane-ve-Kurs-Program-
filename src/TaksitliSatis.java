    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        this.setAlwaysOnTop(false);
        if(this.jLabelAd.getText().equals("") || this.jLabelSoyad.getText().equals("") || this.jLabelNo.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Satış yapmak için listeden öğrenci seçiniz.", null , JOptionPane.DEFAULT_OPTION);                                   
        }
        else if(this.jTextFieldTamTutar.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Tam Tutar boş bırakılamaz.", null , JOptionPane.DEFAULT_OPTION);                       
        }
        else if(dateChooserCombo9.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Lütfen tarih seçiniz.", null , JOptionPane.DEFAULT_OPTION);                                   
        }
        else
        {
            try{
                
                if(Double.parseDouble(this.jTextFieldTamTutar.getText())>0)
                {
                    Object[] options = {"Evet",
                    "Hayır"};
                    int n = JOptionPane.showOptionDialog(this,
                    "Satış yapmak istediğinizden eminmisiniz?Eğer bilgileri kontrol etmek istyorsanız 'Hayır' butonuna basınız.",
                    "",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE,
                    null,
                    options,
                    options[1]);
                    if(n==0)
                    { 
                        (new Functions()).gelirEkle(this.jLabelNo.getText(), this.dateChooserCombo9.getText(), this.jTextFieldTamTutar.getText());                    
                        JOptionPane.showMessageDialog(null, ""+this.jLabelNo.getText()+" numaralı öğrenciye direkt satış yapıldı.", null , JOptionPane.DEFAULT_OPTION);
                    }                                               
                }else{
                    JOptionPane.showMessageDialog(null, "Tam Tutar sıfırdan büyük rakam olmalıdır.", null , JOptionPane.DEFAULT_OPTION);                                                                   
                }
            }catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Tam Tutar sadece rakamlardan ibaret olmalıdır.Ondalıklı sayılarda nokta kullanınız.Mesela 4.3 .", null , JOptionPane.DEFAULT_OPTION);                                   
            }
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButton6ActionPerformed

