    public boolean taksitleriYoxla(){
    
        if(
            this.jTextFieldTutar.getText().equals("") ||
            this.jTextFieldPesinat.getText().equals("") ||
            this.jTextField1Taksit.getText().equals("") ||
            this.jTextField2Taksit.getText().equals("") ||
            this.jTextField3Taksit.getText().equals("") ||
            this.jTextField4Taksit.getText().equals("") ||
            this.jTextField5Taksit.getText().equals("") ||    
            this.jTextField6Taksit.getText().equals("") || 
            this.jTextField7Taksit.getText().equals("") || 
            this.jTextField8Taksit.getText().equals("")             
                ){
                    JOptionPane.showMessageDialog(null, "Taksit değerleri boşluk olamaz", null , JOptionPane.DEFAULT_OPTION);                                   
                    taksitTemizle(); 
                    
        }
        try{
            
            if( Double.parseDouble(this.jTextFieldTutar.getText())<0 ||
            Double.parseDouble(this.jTextFieldPesinat.getText())<0 ||
            Double.parseDouble(this.jTextField1Taksit.getText())<0 ||
            Double.parseDouble(this.jTextField2Taksit.getText())<0 ||
            Double.parseDouble(this.jTextField3Taksit.getText())<0 ||
            Double.parseDouble(this.jTextField4Taksit.getText())<0 ||
            Double.parseDouble(this.jTextField5Taksit.getText())<0 ||
            Double.parseDouble(this.jTextField6Taksit.getText())<0 ||
            Double.parseDouble(this.jTextField7Taksit.getText())<0 ||
            Double.parseDouble(this.jTextField8Taksit.getText())<0 
                    )
            {
                JOptionPane.showMessageDialog(null, "Taksit değerleri , Peşinat ve Tam Tutar sıfırdan büyük olmalıdır.", null , JOptionPane.DEFAULT_OPTION);            
                taksitTemizle();
                return false;
            }
            else if( Double.parseDouble(this.jTextField1Taksit.getText())<=0 &&
            Double.parseDouble(this.jTextField2Taksit.getText())<=0 &&
            Double.parseDouble(this.jTextField3Taksit.getText())<=0 &&
            Double.parseDouble(this.jTextField4Taksit.getText())<=0 &&
            Double.parseDouble(this.jTextField5Taksit.getText())<=0 &&
            Double.parseDouble(this.jTextField6Taksit.getText())<=0 &&
            Double.parseDouble(this.jTextField7Taksit.getText())<=0 &&
            Double.parseDouble(this.jTextField8Taksit.getText())<=0 
                    )
            {
                taksitTemizle();
                JOptionPane.showMessageDialog(null, "Taksit değerleri sıfırdan büyük olmalıdır.Eğer taksitsiz satış yapmak istiyorsanız, Direkt Satış sekmesini seçiniz.", null , JOptionPane.DEFAULT_OPTION);            
                return false;
            }
            return true;
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Taksit değerleri sadece rakamlardan ibaret olmalıdır.", null , JOptionPane.DEFAULT_OPTION);
            taksitTemizle(); 
            return false;
        }
    
    }
    
