    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.setAlwaysOnTop(false);
        if(jTable1.getSelectedRow()!=-1 && jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)!=null)
        {
            Object[] options = {"Evet",
                    "Hayır"};
            int n = JOptionPane.showOptionDialog(this,
            "Sistemden yanlışlıkla sınıfı silmek size çok fazla veri kaybına sebep olur.Sistemden sınıfı silmek istediğinizden eminmisiniz?",
            "",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE,
            null,
            options,
            options[1]);
            if(n==0)
            {
                if((new Functions()).silSinif((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0),(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),1)))
                {
                    JOptionPane.showMessageDialog(null, "Seçtiğiniz sınıf silindi.", null , JOptionPane.WARNING_MESSAGE);                                   
                    this.setTable();
                    this.setComboBoxs();
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Sınıf işlemleri için önce listeden sınıf seçmeniz gerekmektedir.", null , JOptionPane.WARNING_MESSAGE);                       
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButton2ActionPerformed

