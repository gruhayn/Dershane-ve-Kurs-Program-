    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.setAlwaysOnTop(false);
        if(!this.jTextFieldSinifIsmi.getText().equals(""))
        {
            if((new Functions()).sinifEkle(this.jTextFieldSinifIsmi.getText(),String.valueOf(this.jComboBox1.getSelectedItem().toString())))
            {
                JOptionPane.showMessageDialog(null, "Sınıf eklendi.", null , JOptionPane.INFORMATION_MESSAGE);                       
                this.setTable();
            }
            else{
                JOptionPane.showMessageDialog(null,"Bu grupta(ve ya hiç bir grupla ilgisi olmayan) bu isimde sınıf mevcuttur.", null , JOptionPane.ERROR_MESSAGE);                                   
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Sınıf ismi boş bırakılamaz.", null , JOptionPane.INFORMATION_MESSAGE);                               
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTable1.getSelectedRow()!=-1 && jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)!=null)
        {
            SinifListesiOgrenciIslemleri yke=new SinifListesiOgrenciIslemleri();
            yke.jLabelGrupIsmi.setText((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),1));
            yke.jLabelGrupIsmi2.setText((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),1));
            yke.jLabelSinifIsmi.setText((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0));
            yke.jLabelSinifIsmi2.setText((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0));
            yke.setTable2();
            yke.setTable();
            yke.setComboBoxs();
            yke.setVisible(true);
            this.setVisible(false);
            yke.setAlwaysOnTop(true);
        }
        else{
            this.setAlwaysOnTop(false);
            JOptionPane.showMessageDialog(null, "Sınıf işlemleri için önce listeden sınıf seçmeniz gerekmektedir.", null , JOptionPane.WARNING_MESSAGE);                       
            this.setAlwaysOnTop(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

