    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.setAlwaysOnTop(false);
        if(this.jTextFieldGrup.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Grup ismi boş bırakılamaz.", null , JOptionPane.DEFAULT_OPTION);                       
        }
        else{
            boolean var=false;
            String [] grups=(new Functions()).get("GRUP");
            for(String i:grups)
            {
                if(i.equals(this.jTextFieldGrup.getText()))
                {
                    JOptionPane.showMessageDialog(null, "Bu isimde grup mevcuttur.", null , JOptionPane.DEFAULT_OPTION);                       
                    var=true;
                }
            }
            if(!var)
            {
                if((new Functions()).grupEkle(this.jTextFieldGrup.getText()))
                {
                    JOptionPane.showMessageDialog(null, "Grup Eklendi.", null , JOptionPane.DEFAULT_OPTION);                                           
                    this.setTable();
                }
            }
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.setAlwaysOnTop(false);
        if(jTable1.getSelectedRow()!=-1 && jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)!=null)
        {
            Object[] options = {"Evet",
                    "Hayır"};
            int n = JOptionPane.showOptionDialog(this,
            "Grupu silmek istediğinizden eminmisiniz?",
            "",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE,
            null,
            options,
            options[1]);
            if(n==0)
            {
                if((new Functions()).grupSil((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)))
                {
                    JOptionPane.showMessageDialog(null, "Seçilen grup silindi.Sınıfların,Öğrencilerin,Sınavların grupları sıfırlandı.", null , JOptionPane.DEFAULT_OPTION);                                                                               
                    this.setTable();
                }
            }  
        }else{
            JOptionPane.showMessageDialog(null, "Grup silmek için listeden grup seçiniz.", null , JOptionPane.DEFAULT_OPTION);                                                               
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButton2ActionPerformed

