    private void jButtonTamGunDevamsizlikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTamGunDevamsizlikActionPerformed
        this.setAlwaysOnTop(false);
        if(jTable1.getSelectedRow()!=-1 && jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)!=null)
        {
            if((!jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0).equals(""))&& (new Functions()).devamsizlikEkle((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0),"1",(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),1),(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),2),(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),3),(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),4),(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),5),(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),6),this.dateChooserCombo1.getText()))
            {
                JOptionPane.showMessageDialog(null, "Devamsızlık işlendi.", null , JOptionPane.INFORMATION_MESSAGE);                       
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Devamsızlık işlemek için önce listeden öğrenci seçmelisiniz.", null , JOptionPane.ERROR_MESSAGE);                       
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButtonTamGunDevamsizlikActionPerformed

