    private void jButtonDevamsizlikSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDevamsizlikSilActionPerformed
        this.setAlwaysOnTop(false);
        Object[] options = {"Evet",
                    "Hayır"};
            int n = JOptionPane.showOptionDialog(this,
            "Seçtiğiniz öğrencinin tüm devamsızlıklarını silmek istediğinizden eminmisiniz?",
            "",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE,
            null,
            options,
            options[1]);
        if(n==0)
        {
            if(jTable1.getSelectedRow()!=-1 && jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)!=null)
            {
                if((new Functions()).silNoDevamsizlik((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)))
                {
                    JOptionPane.showMessageDialog(null, "Seçtiğiniz öğrencinin tüm devamsızlıkları silindi.", null , JOptionPane.DEFAULT_OPTION);                          
                    this.setTable();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Devamsızlıklarını silmek istediğiniz öğrenciyi listeden seçiniz.", null , JOptionPane.DEFAULT_OPTION);                       
            }
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButtonDevamsizlikSilActionPerformed

