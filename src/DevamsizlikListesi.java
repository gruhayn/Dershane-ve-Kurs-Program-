    private void jButtonDevamsizlikDetayiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDevamsizlikDetayiActionPerformed
        this.setAlwaysOnTop(false);
        if(jTable1.getSelectedRow()!=-1 && jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)!=null)
        {
            DevamsizlikDetayiGor yke=new DevamsizlikDetayiGor();
            yke.setVisible(true);
            this.setVisible(false);
            yke.setAlwaysOnTop(true);
            yke.setOgrNo((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0));
            yke.setOgrIsim((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),1)+" "+(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(),2));
            yke.setTable();
            yke.setToplamGun();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Devamsızlık detayı görmek için önce listeden öğrenci seçmelisiniz.", null , JOptionPane.DEFAULT_OPTION);                       
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButtonDevamsizlikDetayiActionPerformed

